    @property
    def get_ip(self) -> str :
        """get random ip 
        from：https://proxy-randomizer.readthedocs.io/en/latest/readme.html
        """

        rp = RegisteredProviders()
        rp.parse_providers()

        ip_list = [
            f"{proxy.ip_address}:{proxy.port}" for proxy in rp.proxies
            ]
        self.ip = choice(ip_list)

        return self.ip
    

    @property
    def return_usable_ip(self) -> str:
        """測試 ip，直到他可用

        Returns:
            str: 可用的 ip
        """
        # 取得 random ip
        ip = self.get_ip
        test_ip_useful = self.test_ip()
        # 取得可使用 ip
        while test_ip_useful == None:
            ip = self.get_ip
            test_ip_useful = self.test_ip()
        
        return ip
    
    def test_ip(self) -> Dict[str, bool] | None :
        """測試 ip 是否可以使用

        Raises:
            IP_Exception: _description_

        Returns:
            Dict[str, bool]: 回傳 ip、可使用則會回傳 True
        """
        url = "https://data.gov.tw/"

        my_headers = {
                "user-agents":self.get_user_agent
        }

        try:
            # 隨便測試一個網站看此 ip 可不可以用
            resp = requests.get(
                url=url, 
                headers=my_headers,
                proxies={'https': f'{self.ip}'},
                verify=False
                        )
            
            # 可以用就會回傳
            if resp.status_code == requests.codes.ok:
                print(f"成功，此 ip：{self.ip} 可以使用")
                return {
                    self.ip:True
                }
            else:
                raise IP_Exception(f"失敗，此 ip：{self.ip} 不可以使用")
                 
        except:
            IP_Exception(f"失敗，此 ip：{self.ip} 不可以使用")